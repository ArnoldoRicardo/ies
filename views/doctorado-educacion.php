  <h4>Propósitos generales:</h4>
  <p style="text-align: justify">Formar investigadores con un alto nivel acad&#233;mico enfocado a la pr&#225;ctica docente y a la investigaci&#243;n, con capacidades para emprender tareas de innovaci&#243;n y desarrollo educativo que puedan contribuir a la generaci&#243;n de nuevos conocimientos para interpretar y proponer soluciones a los problemas educativos y puedan mejorar la calidad de la educaci&#243;n en el &#225;mbito nacional e internacional.</p>
<h4>Perfil del egresado</h4>
<p style="text-align: justify">El Doctor en Educaci&#243;n. Es el profesional capaz de interpretar y aplicar distintas posturas filos&#243;ficas, sociol&#243;gicas, epistemol&#243;gicas, y que conectan de manera importante con la pr&#225;ctica educativa.</p>
<p style="text-align: justify">Con estas habilidades el egresado ser&#225; capaz de no solo informar sino de trazar estrategias pedag&#243;gicas y did&#225;cticas que favorezcan el desarrollo del saber y de los diversos sujetos implicados en la construcci&#243;n de una visi&#243;n del mundo mucho mas objetiva.</p>
<h4>Conocimientos:</h4>
<ul class="arrow dotted">
  <li>Especializados en &#225;reas de docencia e investigaci&#243;n educativa.</li>
  <li>An&#225;lisis de la relaci&#243;n ense&#241;anza-aprendizaje con una dimensi&#243;n cr&#237;tica que desarrolla en el individuo una cosmovisi&#243;n mas amplia y robustecida.</li>
  <li>Crear modelos hacia la comunidad de cuestionamiento e investigaci&#243;n con instancia que conlleva hacia aprendizajes significativos.</li>
  <li>Para incorporar los aportes que se producen en el contexto nacional e internacional respecto al debate curricular actual.</li>
  <li>Conocimientos amplios para la elaboraci&#243;n, evaluaci&#243;n y monitoreo de Proyectos educativos.</li>
  <li>Amplios de las &#225;reas fundamentales de la educaci&#243;n.</li>
</ul>
<h4>Habilidades:</h4>
<ul class="arrow dotted">
  <li>Generar y aplicar estrategias para solucionar problemas educativos, aplicando en forma interdisciplinaria, los conocimientos adquiridos.</li>
  <li>Identificar estrategias epistemol&#243;gicas y metodol&#243;gicas de investigaci&#243;n que respondan a la complejidad de los fen&#243;menos educativos.</li>
  <li>Para comprender los estudios del curriculum desde la perspectiva critica.</li>
  <li>Para el diagnostico y la evaluaci&#243;n de los procesos educativos, en contextos sociales nacionales.</li>
  <li>En uso de la tecnolog&#237;a para la actualizaci&#243;n y desarrollo de proyectos de investigaci&#243;n educativa.</li>
  <li>Generar proyectos innovadores que mejoren la calidad del proceso educativo.</li>
  <li>Para investigar problemas educativos relacionados con su pr&#225;ctica.</li>
  <li>Aportar innovaciones al campo de la educaci&#243;n, principalmente mediante la investigaci&#243;n aplicada.</li>
</ul>
<h4>Actitudes:</h4>
<ul class="arrow dotted">
  <li>Compromiso social y actitud &#233;tica que gu&#237;en sus decisiones en beneficio de la educaci&#243;n.</li>
  <li>Actitud de colaboraci&#243;n y trabajo en equipo.</li>
  <li>Actitud para establecer un dialogo eficaz con distintos campos disciplinarios.</li>
  <li>Actitud creativa para generar y probar alternativas innovadoras.</li>
  <li>Actitud de confianza y seguridad en si mismo, fundamentalmente por su capacidad y conocimiento.</li>
  <li>Actitud constante de aprendizaje.</li>
</ul>
