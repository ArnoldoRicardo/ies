<p class="success"><strong>Acuerdo: </strong>PSU-150/2008</p>
<h4>Proposito general:</h4>
<p style="text-align: justify">Formar especialistas,  investigadores, docentes y expertos de excelencia en el campo de las ciencias juridico penales,  con pleno conocimiento de la legislaci&#243;n y su aplicaci&#243;n en la sociedad,  lo cual le permitira una participaci&#243;n activa, critica y creativa, tanto a nivel local, nacional como internacional.</p>
<h4>Perfil del egresado</h4>
<p style="text-align: justify">El egresado de la maestria en ciencias penales, sera un especialista que contara con: una s&#243;lida formaci&#243;n, te&#243;rica, anal&#237;tica y metodol&#243;gica, con habilidades y destrezas que le permitan aplicar sus conocimientos en el ambito profesional con la creatividad, responsabilidad y el profesionalismo que hoy demanda el ejercicio jur&#237;dico. </p>
<p style="text-align: justify">Sera un ente  que procura y administra las normas juridicas penales que regulan la conducta del ser humano con la finalidad de asegurar los valores elementales sobre los cuales descansa la convivencia humana, teniendo con fin &#250;ltimo proteger a la sociedad, asimismo estar&#225; capacitado para regular la potestad p&#250;blica de castigar a los autores de infracciones punibles y aplicar las medidas de seguridad pertinentes para la prevenci&#243;n de conductas delictuosas.</p>
<h4>Conocimientos:</h4>
<ul class="arrow dotted">
<li>Diagnosticar sobre medidas de seguridad que garanticen el bienestar de la sociedad.</li>
<li>Regular la potestad p&#250;blica para imponer las sanciones a los infractores de una norma jur&#237;dica.</li>
<li>Administrar las normas que regulan las conductas del ser humano.</li>
<li>Interpretar el conjunto de normas jur&#237;dicas establecidas por el estado, que asocian el crimen como hecho, a la Pena como leg&#237;tima consecuencia.</li>
<li>Conocer y manejar la legislaci&#243;n del fuero com&#250;n y  federal, as&#237; como los tratados internacionales.</li>
<li>Administrar la justicia de manera imparcial y conforme a derecho.</li>
<li>Analizar los diferentes instrumentos legales con que se cuentan en nuestra entidad y estado.</li>
<li>Acrecentar  la creatividad y profesionalismo que demanda el ejercicio jur&#237;dico.</li>
<li>Contar con herramientas para acceder a la formulaci&#243;n y/o modificaci&#243;n de las normas relacionadas al derecho penal.</li>
<li>Identificar la importancia de crear y ejecutar las normas jur&#237;dicas con equidad de g&#233;nero.</li>
<li>Garantizar la defensa de los individuos contra otros individuos e incluso contra el propio estado.</li>
</ul>
<h4>Habilidades:</h4>
<ul class="arrow dotted">
<li>Litigar con pleno conocimiento de las normas jur&#237;dicas legalmente establecidas.</li>
<li>Generar un ambiente de armon&#237;a y cordialidad entre los actores  involucrados en la litis.</li>
<li>Proponer soluciones conciliatorias a los conflictos entre individuos.</li>
<li>Proponer e implementar medidas de seguridad que garanticen el bienestar de la sociedad.</li>
<li>Aportar innovaciones al marco jur&#237;dico establecido mediante el pleno conocimiento de los factores que influyen en la sociedad.</li>
<li>Sistematizar los procesos penales.</li>
<li>Aplicar estrategias que garanticen una administraci&#243;n de justicia apegada a las normas jur&#237;dicas legalmente establecidas.</li>
<li>Aplicar la creatividad y profesionalismo que demanda en el ejercicio jur&#237;dico.</li>
<li>Aplicar los ordenamientos jur&#237;dicos respetando la equidad de g&#233;nero.</li>
<li>Acceder a los &#243;rganos jurisdiccionales con propuestas viables para la resoluci&#243;n de conflictos.</li>
</ul>
<h4>Actitudes:</h4>
<ul class="arrow dotted">
<li>Actitud de colaboraci&#243;n y trabajo en equipo</li>
<li>Actitud creativa para generar y probar alternativas innovadoras</li>
<li>Actitud concertadora para integrar diferentes puntos de vista y conseguir la buena disposici&#243;n de todas las partes interesadas.</li>
<li>Actitud de confianza y seguridad en si mismo, fundamentalmente por su capacidad y conocimiento.</li>
<li>Actitud constante de aprendizaje.</li>
<li>Actitud de disposici&#243;n y deseos de trabajar en beneficio de la sociedad.</li>
<li>Actitud de rectitud y transparencia en el ejercicio de sus funciones.</li>
</ul>
