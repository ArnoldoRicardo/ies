<h4>Proposito general:</h4>
<p style="text-align: justify">Formar profesionales capaces de ayudar a las personas f&#237;sicas y morales a prevenir o resolver conflictos de car&#225;cter jur&#237;dico, que procuren incesantemente la justicia, la libertad y la conservaci&#243;n del derecho como pilares insustituibles del bien com&#250;n y que adem&#225;s difundan la cultura jur&#237;dica para facilitar la comprensi&#243;n del derecho entre los no especialistas.</p>
<h4>Objetivos:</h4>
<ul class="arrow dotted">
<li> Preparar profesionales de alto nivel acad&#233;mico en el &#225;rea  <strong>jur&#237;dica.</strong></li>
<li>Atender a la demanda de <strong>educaci&#243;n superior</strong> formando profesionistas de alto nivel que respondan a las necesidades de <strong>desarrollo socioecon&#243;mico</strong> de la entidad y del pa&#237;s.</li>
<li>Desarrollar recursos humanos con capacidad innovadora y dominio de conocimientos, <strong>t&#233;cnicas y habilidades</strong> que le permitan desarrollar un trabajo eficiente en las  organizaciones. </li>
<li>Convertirnos en agentes de Cambio, mediante la permanente vinculaci&#243;n con la comunidad, en especial en los sectores productivos de bienes y servicios, social, p&#250;blico y privado.</li>
<li>Formar profesionales con elevada calidad acad&#233;mica, capaces de trabajar en proyectos de investigaci&#243;n que impulsen el desarrollo en nuestra regi&#243;n, en nuestra entidad y en M&#233;xico.</li>
</ul>
<h4>Perfil del egresado</h4>
<p>La formaci&#243;n profesional  del Licenciado en Derecho identifica  y analiza las necesidades jur&#237;dicas, con el fin de satisfacerlas a trav&#233;s de estrategias adecuadas de defensa.</p>
<h4>Conocimientos y habilidades</h4>
<ul class="star dotted">
<li><strong>Aptitudes:</strong> Resultan indispensables las intelectuales, verbales y jur&#237;dicas; son necesarias las criticas y anal&#237;ticas.</li>
<li><strong>Intereses:</strong> Son indispensables los de oficina y actividades persuasivas; necesarios los intereses de c&#225;lculo y cient&#237;ficos. En ciertas condiciones pudieran necesitarse los mec&#225;nicos, de servicio social y literario.</li>
<li><strong>Valores:</strong> Son necesarios los valores econ&#243;micos, sociales, te&#243;ricos y pol&#237;ticos.</li>
<li><strong>Habilidades:</strong> An&#225;lisis, s&#237;ntesis y soluci&#243;n de problemas, comunicaci&#243;n oral y escrita, concertaci&#243;n, desarrollo organizacional, direcci&#243;n de personal, manejo de grupos, toma de decisiones, relaciones humanas, soluci&#243;n de conflictos laborales, liderazgo.</li>
</ul>
