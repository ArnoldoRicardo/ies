  <p style="text-align: justify">Preparar a los profesionales del sector educativo y a los profesionistas interesados en esta materia con un alto nivel acad&#233;mico enfocado a la practica docente y a la investigaci&#243;n, para optimizar  el proceso educativo a trav&#233;s de la experiencia de aprendizaje especificas que desarrollen sus conocimientos, destrezas y valores en su comportamiento profesional, que puedan mejorar la calidad de la educaci&#243;n en el estado de Chiapas.</p>
<h4>Perfil del egresado</h4>
<p style="text-align: justify">El maestro en ciencias de la educaci&#243;n. Es el profesional capaz de conocer e interpretar distintas posturas filos&#243;ficas, sociol&#243;gicas, epistemol&#243;gicas, y que conectan de manera importante con la pr&#225;ctica  educativa.</p>
<p style="text-align: justify">Con estas habilidades el egresado  ser&#225; capaz de no solo informar sino de trazar estrategias pedag&#243;gicas y did&#225;cticas que favorezcan el desarrollo del saber y de los diversos sujetos implicados en la construcci&#243;n de una visi&#243;n del mundo mucho mas  objetiva.</p>
