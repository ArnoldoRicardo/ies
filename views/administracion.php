<h4>Objetivo general:</h4>
<ul class="arrow dotted">
<li>Formar profesionales altamente calificados para el dise&#241;o de las pol&#237;ticas de organizaci&#243;n, utilizaci&#243;n eficaz de los recursos humanos, materiales, te&#243;ricos y de control de las diversas actividades que se realizan en la organizaci&#243;n, conforme a los planes estrat&#233;gicos establecidos y el mantenimiento de las mejores condiciones de la estructura administrativa empresarial para responder de manera adecuada a las necesidades del cambio y la transformaci&#243;n.</li>
<li>Formar profesionales en el campo de las ciencias de la administraci&#243;n de empresas para aplicar con calidad sistemas de informaci&#243;n automatizados que permitan tomar decisiones &#225;giles y oportunas de los procesos de organizaci&#243;n en instituciones bancarias, estatales, privadas, organismos no gubernamentales, centros de capacitaci&#243;n y consultor&#237;a.</li>
<li>Garantizar el dominio de la teor&#237;a administrativa para el desarrollo del personal, hacer m&#225;s eficiente la organizaci&#243;n y aplicar t&#233;cnicas e instrumentos que contribuyan a la formulaci&#243;n, evaluaci&#243;n y gesti&#243;n de proyectos empresariales, gubernamentales y sociales.</li>
<li>Desarrollar la capacidad de esp&#237;ritu emprendedor y creador.</li>
</ul>
<h4>Perfil del egresado:</h4>
<ul class="arrow dotted">
<li>Adquirir una s&#243;lida formaci&#243;n multidisciplinaria que incluya conocimientos profundos de contabilidad, administraci&#243;n y econom&#237;a; as&#237; como del derecho, matem&#225;ticas y humanidades para que le permitan resolver problemas b&#225;sicos de la profesi&#243;n de las ciencias de la administraci&#243;n de empresas.</li>
<li>Desempe&#241;arse en un contexto econ&#243;mico, social, pol&#237;tico y cultural complejo, caracterizado por el surgimiento de nuevos bloques regionales, en el contexto del creciente desarrollo de la globalizaci&#243;n.</li>
<li>Realizar funciones directivas de an&#225;lisis, planeaci&#243;n, organizaci&#243;n, coordinaci&#243;n y control de las diversas empresas.</li>
<li>Actuar en el desempe&#241;o gerencial de empresas p&#250;blicas, sociales y privadas en las &#225;reas y funciones que le son propias a las ciencias de la administraci&#243;n de empresas.</li>
<li>Intervenir en la definici&#243;n de las misiones, de los objetivos y las pol&#237;ticas de las diversas organizaciones sean estas p&#250;blicas, privadas o sociales.</li>
<li>Dise&#241;ar y asesorar en materia de estructuras, sistemas y procesos administrativos.</li>
<li>Planificar, dirigir y controlar la implantaci&#243;n de sistemas de informaci&#243;n administrativa de las organizaciones.</li>
<li>Intervenir en tareas de consultor&#237;a y administraci&#243;n de personal en &#225;reas de selecci&#243;n, evaluaci&#243;n, capacitaci&#243;n, motivaci&#243;n y dem&#225;s aspectos vinculados al factor humano en la empresa.</li>
<li>Formular y administrar el presupuesto, la evaluaci&#243;n de proyectos de inversi&#243;n y los estudios de factibilidad econ&#243;micos-financieros en empresas privadas y p&#250;blicas.</li>
<li>Elaborar e implantar pol&#237;ticas, sistemas, m&#233;todos de comercializaci&#243;n, dise&#241;ar y conducir estudios de mercados, procesos de log&#237;stica, producci&#243;n y comercializaci&#243;n de bienes y servicios.</li>
<li>Dise&#241;ar proyectos, programas y planes de negocios.</li>
<li>Asesorar en materia de responsabilidad social en las organizaciones, interviniendo en la evaluaci&#243;n de los impactos sociales y ambientales de las decisiones de las empresas e informar a sus directivos sobre las medidas posibles para preservar la calidad de vida y el medio ambiente.</li>
<li>Evaluar las funciones de planeaci&#243;n, conducci&#243;n, coordinaci&#243;n y evaluaci&#243;n, como consultor y directivo, en todo tipo de organizaciones.</li>
<li>Desempe&#241;arse con idoneidad y &#233;tica profesional en cualquiera de los campos del ejercicio profesional de las ciencias de la administraci&#243;n de empresas.</li>
</ul>
